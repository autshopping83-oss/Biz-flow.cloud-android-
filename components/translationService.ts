export const formatMoney = (amount: number, currencyCode: string, lang?: string) => {
  try {
    return new Intl.NumberFormat(lang, {
      style: 'currency',
      currency: currencyCode,
    }).format(amount);
  } catch (e) {
    // Fallback if currency code is invalid
    return `${amount.toFixed(2)} ${currencyCode}`;
  }
};
